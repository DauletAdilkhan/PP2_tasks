"""sort()"""
#Sort the list alphabetically:
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)
#Sort the list descending:
thislist.sort(reverse = True)
print(thislist)



#Sort the list numerically:
thislist1 = [100, 50, 65, 82, 23]
thislist1.sort()
print(thislist1)
#Sort the list descending:
thislist1.sort(reverse = True)
print(thislist1)